package cn.tju.modelsearch.domain;

public class sum {
    private int login;
    private int regis;
    private int detail;
    private int download;
    private String date;

    public int getLogin() {
        return login;
    }

    public void setLogin(int login) {
        this.login = login;
    }

    public int getRegis() {
        return regis;
    }

    public void setRegis(int regis) {
        this.regis = regis;
    }

    public int getDetail() {
        return detail;
    }

    public void setDetail(int detail) {
        this.detail = detail;
    }

    public int getDownload() {
        return download;
    }

    public void setDownload(int download) {
        this.download = download;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
